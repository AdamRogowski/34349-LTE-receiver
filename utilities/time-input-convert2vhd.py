import re

def to_complex(real, imag):
    return f"to_complex({real:.6f}, {imag:.6f})"

def convert_text(input_text):
    lines = input_text.strip().split('\n')
    complex_numbers = []
    for i, line in enumerate(lines):
        real, imag = map(float, re.findall(r"[-+]?\d*\.\d+|\d+", line))
        complex_numbers.append(to_complex(real, imag))
        if i < len(lines) - 1:
            complex_numbers.append(",\n")
    return "(\n" + " ".join(complex_numbers) + ");"

